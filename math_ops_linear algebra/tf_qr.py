import tensorflow as tf

"""tf.qr(input, full_matrices=None, name=None)
功能：对矩阵进行qr分解。
输入：。"""

a = tf.constant([1, 2, 2, 1, 0, 2, 0, 1, 1], shape=[3, 3], dtype=tf.float64)
q, r = tf.qr(a)

sess = tf.Session()
print(sess.run(tf.qr(a)))
sess.close()

